package org.cytoscape.clustnsee3.internal.gui.dialog;

import java.util.Vector;

import javax.swing.table.AbstractTableModel;

/**
 * 
 */
public class CnSContingencyTableModel extends AbstractTableModel {
	private static final long serialVersionUID = -5638398154610258836L;
	private Vector<String> colName;
	private Vector<Vector<Integer>> data;
	
	/**
	 * @param
	 * @return
	 */
	public CnSContingencyTableModel(Vector<Vector<Integer>> data) {
		super();
		colName = new Vector<String>();
		this.data = data;
	}
	
	/* (non-Javadoc)
	 * @see javax.swing.table.TableModel#getColumnCount()
	 */
	@Override
	public int getColumnCount() {
		return colName.size();
	}

	/* (non-Javadoc)
	 * @see javax.swing.table.TableModel#getRowCount()
	 */
	@Override
	public int getRowCount() {
		return data.size();
	}

	/* (non-Javadoc)
	 * @see javax.swing.table.TableModel#getValueAt(int, int)
	 */
	@Override
	public Object getValueAt(int x, int y) {
		return data.elementAt(x).elementAt(y);
	}
	
	/* (non-Javadoc)
	 * @see javax.swing.table.TableModel#getColumnClass(int)
	 */
	@Override
	public Class<?> getColumnClass(int columnIndex) {
		if (columnIndex == 0)
			return String.class;
		else
			return Integer.class;
	}
	
	/* (non-Javadoc)
	 * @see javax.swing.table.TableModel#isCellEditable(int, int)
	 */
	@Override
	public boolean isCellEditable(int rowIndex, int columnIndex) {
		return false;
	}

	/* (non-Javadoc)
	 * @see javax.swing.table.TableModel#getColumnName(int)
	 */
	@Override
	public String getColumnName(int columnIndex) {
		return colName.elementAt(columnIndex);
	}
}
